# Integer, String, Float, Boolean, None -> these are datatypes used in python
age = 21 # integere
print(type(age))

name = "yash" # string
print(type(name))

pi = 3.14 # float
print(type(pi))

flag = True # Boolean -> can have value True or False
print(type(flag))

isprime = None # None type when we doesnt want to assign any type to variable we assign None
print(type(isprime))